    private void tblEspectaculosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEspectaculosMousePressed
        if(evt.getClickCount() == 2)
        {
            int fila = tblEspectaculos.getSelectedRow();
            String nombre = (String) tblEspectaculos.getValueAt(fila,0);//selecciono de la fila seleccionada la primera
            
            int resultado = JOptionPane.showConfirmDialog(null, "Â¿Desea aceptar el espectaculo "+nombre+"?","Aceptar o rechazar espectaculo",JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            
            if(resultado >= 0 && resultado < 2)//Es decir que coloco que si o que cancelo
            {
                String estado = "A";//Aceptado
                if(resultado == 1)//Rechazado
                {
                    estado = "R";
                }
                ICE.actualizarEstadoEspectaculo(nombre,estado);
                if(estado == "A")
                {
                    JOptionPane.showMessageDialog(this, "Aceptado correctamente.");
                }else if(estado == "R"){
                    JOptionPane.showMessageDialog(this, "Rechazado correctamente.");
                }
                
                cargarTabla();
            }
        }   
    }//GEN-LAST:event_tblEspectaculosMousePressed

