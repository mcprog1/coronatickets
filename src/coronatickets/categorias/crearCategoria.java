    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String nombre = txtNombre.getText();
        boolean continuar = true;
        if(nombre.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "El nombre es obligatorio.");
            continuar = false;
        }
        if(continuar) //Es que esta todo OK
        {
            String resultado = ICC.crearCategoria(nombre);
            if(resultado == "S")//Todo OK
            {
                JOptionPane.showMessageDialog(this, "Creado con exito.");
                dispose();
            }else{
                if(resultado == "A") //Ya existe ese nombre
                {
                    JOptionPane.showMessageDialog(this, "El nombre ya exist√≠a, se actulizo correctamente.");
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Erro a la hora de crear la plataforma.");
                }
            }
        }

    }//GEN-LAST:event_btnCrearActionPerformed

