    private void CargarDatos() throws SQLException, ClassNotFoundException, IOException {

        ArrayList<Funciones> lista = new ArrayList();

        lista = ICF.ListarFuncionesEspectaculo(SeleccionarEspectaculoCU8.lstEspetaculo.getSelectedValue());

        DefaultTableModel model = (DefaultTableModel) tblFunciones.getModel();

        model.setRowCount(0);

        for (Funciones f : lista) {
            
            Timestamp hora = f.getHoraDeInicio();
            SimpleDateFormat sdf = new SimpleDateFormat("hh:mm");
            
            Object[] fila = {f.getNombre(), f.getFecha(), sdf.format(hora)};
            model.addRow(fila);
        }
    }
